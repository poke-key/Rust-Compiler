#[derive(Debug)]
enum IRTok {
    // func
    Func,
    EndFunc,

    // declarations.
    Int,
    IntArray,

    // function calling routines.
    Call,
    Return,

    // input/output routines.
    Out,
    In,

    // mathematical operators.
    Mov,
    Add,
    Sub,
    Mult,
    Div,
    Mod,

    // comparison operators.
    LessThan,
    LessEqual,
    NotEqual,
    Equal,
    GreaterEqual,
    GreaterThan,

    // labels/branching
    Label(String),
    Jump,
    BranchIf,
    BranchIfNot,

    Comma,
    LBrace,
    RBrace,
    LParen,
    RParen,
    Plus,

    EndInstr,

    InvalidInstruction(String),

    Num(i32),
    Var(String),
}

#[derive(Debug, Clone)]
enum Op {
    Num(i32),
    Var(i32),
}

#[derive(Debug)]
enum VariableType {
    IntVar(i32),
    ArrayVar(i32, i32),
}

#[derive(Debug)]
enum MemWrite {
    IntVar(i32),
    ArrayWrite(i32, Op),
}

#[derive(Debug)]
enum MemRead {
    IntVar(i32),
    Number(i32),
    ArrayRead(i32, Op),
}

